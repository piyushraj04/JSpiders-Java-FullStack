class BitwiseOperators2
{
	public static void main(String [] args)
	{
		int x = 25;
		int y = 21;
		
		System.out.println("The result is of BITWISE AND is : " + x&y);
		System.out.println("The result is of BITWISE OR is : " + x|y);
		System.out.println("The result is of BITWISE AND is : " + x^y);
	}
}